package com.neusoft.ruigou.po;

public class Merchant {
	private Integer merchantId;
	private String merchantPwd;
	private String merchantName;
	private Integer merchantPhone;
	private String merchantTime;
	private String merchantType;
	private String merchantAddress;
	private String service;
	public Integer getMerchantId() {
		return merchantId;
	}
	public void setMerchantId(Integer merchantId) {
		this.merchantId = merchantId;
	}
	public String getMerchantPwd() {
		return merchantPwd;
	}
	public void setMerchantPwd(String merchantPwd) {
		this.merchantPwd = merchantPwd;
	}
	public String getMerchantName() {
		return merchantName;
	}
	public void setMerchantName(String merchantName) {
		this.merchantName = merchantName;
	}
	public Integer getMerchantPhone() {
		return merchantPhone;
	}
	public void setMerchantPhone(Integer merchantPhone) {
		this.merchantPhone = merchantPhone;
	}
	public String getMerchantTime() {
		return merchantTime;
	}
	public void setMerchantTime(String merchantTime) {
		this.merchantTime = merchantTime;
	}
	public String getMerchantType() {
		return merchantType;
	}
	public void setMerchantType(String merchantType) {
		this.merchantType = merchantType;
	}
	public String getMerchantAddress() {
		return merchantAddress;
	}
	public void setMerchantAddress(String merchantAddress) {
		this.merchantAddress = merchantAddress;
	}
	public String getService() {
		return service;
	}
	public void setService(String service) {
		this.service = service;
	}
	
}
