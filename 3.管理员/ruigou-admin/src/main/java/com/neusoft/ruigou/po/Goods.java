package com.neusoft.ruigou.po;

public class Goods {
	private Integer goodsId;
	private String goodsName;
	private String goodsClass;
	private Integer goodsPrice;
	private Integer goodsStore;
	private Integer goodsbank;
	private Integer merchantID;
	
	private Integer goodssalenumber;
	private String goodsImg;
	private String goodsDetails;
	private String goodsStatus;
	
	public Integer getGoodsId() {
		return goodsId;
	}
	public void setGoodsId(Integer goodsId) {
		this.goodsId = goodsId;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public String getGoodsClass() {
		return goodsClass;
	}
	public void setGoodsClass(String goodsClass) {
		this.goodsClass = goodsClass;
	}
	public Integer getMerchantID() {
		return merchantID;
	}
	public void setMerchantID(Integer merchantID) {
		this.merchantID = merchantID;
	}
	public String getGoodsStatus() {
		return goodsStatus;
	}
	public void setGoodsStatus(String goodsStatus) {
		this.goodsStatus = goodsStatus;
	}
	public Integer getGoodsPrice() {
		return goodsPrice;
	}
	public void setGoodsPrice(Integer goodsPrice) {
		this.goodsPrice = goodsPrice;
	}
	public Integer getGoodsStore() {
		return goodsStore;
	}
	public void setGoodsStore(Integer goodsStore) {
		this.goodsStore = goodsStore;
	}
	public Integer getGoodsbank() {
		return goodsbank;
	}
	public void setGoodsbank(Integer goodsbank) {
		this.goodsbank = goodsbank;
	}
	public Integer getGoodssalenumber() {
		return goodssalenumber;
	}
	public void setGoodssalenumber(Integer goodssalenumber) {
		this.goodssalenumber = goodssalenumber;
	}
	public String getGoodsImg() {
		return goodsImg;
	}
	public void setGoodsImg(String goodsImg) {
		this.goodsImg = goodsImg;
	}
	public String getGoodsDetails() {
		return goodsDetails;
	}
	public void setGoodsDetails(String goodsDetails) {
		this.goodsDetails = goodsDetails;
	}
	
	
}
